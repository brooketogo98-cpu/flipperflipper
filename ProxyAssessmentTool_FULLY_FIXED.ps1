        private int CompareVersions(string v1, string v2)
        {
            int[] parts1 = Array.ConvertAll(v1.Split('.'), int.Parse);
            int[] parts2 = Array.ConvertAll(v2.Split('.'), int.Parse);
            
            for (int i = 0; i < Math.Max(parts1.Length, parts2.Length); i++)
